#Volume of Square Pyramid.

b = int(input("Enter Height Of Square Pyramid : "))
h = int(input("Enter Base Of Square Pyramid   : "))

v = (1/3) * b * b * h 

print("="*30)
print("   Volume of Square Pyramid")
print("="*30)
print(" Base   :",b,"cm.")
print(" Height :",h,"cm.")
print(" Volume :",v,"sq. cm.")
print("="*30)

#Surface Area Of Cylinder

rad = int(input("Enter Radius Of Cylinder : "))
h = int(input("Enter Height Of Cylinder   : "))

area = 2 * 3.14 * rad * rad * h 

print("="*30)
print("   Surface Area of Cylinder")
print("="*30)
print(" Radius :",rad,"cm")
print(" Height :",h,"cm")
print(" Area   :",area,"sq. cm.")
print("="*30)


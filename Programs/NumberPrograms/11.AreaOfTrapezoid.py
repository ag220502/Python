#Area of Trapezoid
base1 = int(input("Enter Base 1 Of Triangle : "))
base2 = int(input("Enter Base 2 Of Triangle : "))
height = int(input("Enter Height Of Triangle : "))

area = (base1 + base2) * height

print("="*25)
print("    Area Of Trapezoid")
print("="*25)
print(" Base 1 : ",base1,"cm.")
print(" Base 2 : ",base2,"cm.")
print(" Height : ",height,"cm.")
print(" Area   : ",area,"sq. cm.")
